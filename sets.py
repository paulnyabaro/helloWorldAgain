# Sets are just dictionaries without key value pair
vegetable = {"carrot", "lettuce", "cabbage", "banana", "carrot"}
print(vegetable)

# Defining an empty set
set()

# Adding to a set
# Using add method
vegetable.add("onion")
print(vegetable)

# Using update method
vegetable.update(["potato", "pumpkin"])
print(vegetable)
